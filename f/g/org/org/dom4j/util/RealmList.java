package f.g.org.org.dom4j.util;

import f.g.b.a.g.h;

@h
public class RealmList
  extends HttpRequest
{
  public String realm;
  
  public RealmList(String paramString)
  {
    super(paramString);
  }
  
  public Namespace get()
  {
    Namespace localNamespace = super.get();
    realm = realm;
    return localNamespace;
  }
}
